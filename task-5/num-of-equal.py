# Програма для визначення кількості однакових чисел серед трьох цілих чисел

# Введення трьох цілих чисел
a = int(input("Введіть перше число: "))
b = int(input("Введіть друге число: "))
c = int(input("Введіть третє число: "))

# Логіка для визначення кількості однакових чисел4
if a == b == c:
    print(3)  # Всі три числа однакові
elif a == b or a == c or b == c:
    print(2)  # Два числа однакові
else:
    print(0)  # Всі числа різні