# Програма для перевірки, чи може шаховий кінь потрапити з однієї клітини на іншу одним ходом

# Введення координат першої клітини
x1 = int(input("Введіть номер стовпця першої клітини (1-8): "))
y1 = int(input("Введіть номер рядка першої клітини (1-8): "))

# Введення координат другої клітини
x2 = int(input("Введіть номер стовпця другої клітини (1-8): "))
y2 = int(input("Введіть номер рядка другої клітини (1-8): "))

# Перевірка, чи може кінь зробити хід
dx = abs(x1 - x2)
dy = abs(y1 - y2)

if (dx == 2 and dy == 1) or (dx == 1 and dy == 2):
    print("Так, кінь може потрапити з першої клітини на другу одним ходом.")
else:
    print("Ні, кінь не може потрапити з першої клітини на другу одним ходом.")