import random

# Введення початкової кількості елементів у списку
n = int(input("Введіть кількість елементів у списку: "))

# Генеруємо список із n цілих чисел від 0 до 20
lst = [random.randint(0, 20) for _ in range(n)]
print("Згенерований список:", lst)

# Введення індексу k та значення C
k = int(input(f"Введіть індекс k для вставки (0-{n}): "))
C = int(input("Введіть значення нового елемента C: "))

# Додаємо будь-який елемент у кінець списку, щоб збільшити розмір
lst.append(0)

# Зсуваємо елементи вправо, починаючи з кінця до k+1
for i in range(len(lst)-1, k, -1):
    lst[i] = lst[i-1]

# Вставляємо новий елемент на позицію k
lst[k] = C

print("Список після вставки:", lst)